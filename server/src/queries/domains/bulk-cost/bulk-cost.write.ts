export function buildSetBulkCostRunRevisionGroupSql(tableName: string): string {
    return `
UPDATE ${tableName}
SET [RevisionGroupID] = @RunID
WHERE [RunID] = @RunID
  AND [RevisionGroupID] IS NULL;
`;
}

